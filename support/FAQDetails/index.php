                                    <h5>What should I do if there is no picture on multiple channels?</h5>
                                    
                                    <hr>
                                    
                                    <div class="answer">
                                        <div class="twc-base-text section">
                                            <b>Check Other TVs in Your Home</b>
                                            <p>If you have other TVs, check their reception. If they all have no picture reception, please <a href="/en/residential-home/support/contact-us.html">Contact Us</a>.</p>
                    
                                            <b>Verify Connections</b>
                                            <p>Are all connections secured and have you checked that the other end of the cable is securely plugged in? Are you using the proper connectors and color coding? If you are using a cable set-top box, the cable coming from wall should be connected to the &quot;IN&quot; jack on the cable box.</p>
                                            <p>If you're not using a cable box, the cable coming from the wall should be connected to the &quot;IN&quot; jack on the VCR, DVD player or directly to the TV.</p>
                    
                                            <b>Check the TV</b>
                                            <p>Is the proper input selected? (Channel 3, Input 1, Input 2, HDMI, etc.)</p>
                                            <p>Are you an HD subscriber? If so, make sure your TV is on the correct input, i.e., component, HDMI, DVI.</p>
                                            <p>No HD? Please ensure your TV is on channel 3.</p>
                    
                                            <b>Check the Cable Box</b>
                                            <p>Is the cable box power on?</p>
                                            <p>Check Your VCR, DVD and/or Game Device</p>
                                            <p>If any of these devices are connected to your TV, please make sure the power is off for those devices. Check to make sure your TV is set to the right input.</p>
                    
                                            <b>Reboot the Cable Box</b>
                                            <ol>
                                                <li>Unplug the power cord from the back of the cable box.</li>
                                                <li>Wait 30 seconds.</li>
                                                <li>Plug the power cord back into the back of the cable box. The cable box will reboot automatically; it should take no more than 20 minutes.</li>
                                                <li>Wait for the time or channel to reappear on front of the cable box.</li>
                                                <li>Wait 3 minutes to allow the services to reset.</li>
                                                <li>Turn on the TV.</li>
                                            </ol>
                                            <p><strong>Note:</strong> If this happens often, you might have a signal issue. If so, please <a href="/en/residential-home/support/contact-us.html">Contact Us</a>.</p>
                    
                                            <b>Still No Picture?</b>
                                            <p>Automated phone self help is available to help diagnose your cable TV problem, and send a signal to your cable box to fix the problem.&nbsp; If so, please <a href="/en/residential-home/support/contact-us.html">Contact Us</a>.</p>
                                            <p>If phone self help does not resolve your cable TV problem, let the customer support representative know:</p>
                                            <div class="bulleted">
                                                <ul>
                                                    <li>You went through the online troubleshooting</li>
                                                    <li>Your TV is on and working properly</li>
                                                    <li>Your cable box is on</li>
                                                    <li>Your connection and inputs are correct</li>
                                                    <li>You completed phone self help<br>
                                                    </li>
